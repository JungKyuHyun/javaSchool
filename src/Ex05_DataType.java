
public class Ex05_DataType {
	public static void main(String[] args) {
		// 안에 선언되는 변수 지역변수 (main 함수 안쪽)
		
		byte b = 100;
		System.out.println(b);
		
		int i = 12345;
		System.out.println(i);
		
		long l = 10000000000l; // 정수 리터럴은 int
		
		float f = 3.14f; // 실수 리터럴은 double
		
		double d = 3.141592;
		
		boolean bo = true;
		
		char ch = 'A';
		
		String str = "홍길동";
	}

}
